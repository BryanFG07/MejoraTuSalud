        <div class="procedimiento">
            <center><h3>Procedimiento</h3></center>
            &nbsp;&nbsp;&nbsp;&nbsp;Paso 1. Empieza la receta de pollo a la portuguesa con papas pelando la cebolla y la zanahoria. Corta la cebolla y el ají en juliana, la zanahoria en rodajas. Saltea la cebolla con 1 cucharadita de aceite o aceite en aerosol y con el fuego alto, cuando se dore, baja el fuego a medio y agrega 1 cucharada de agua. Por otro lado, cuando se evapore el agua, agrega al ají y la zanahoria. Cada vez que se empiece a dorar, añade 1 cucharada de agua para que se cocine poco a poco, pero con el sabor del salteado <br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 2. Mientras las verduras se cocinan, corta la suprema en 3 porciones y pásalas por harina o maicena. De esta manera, se dorarán por fuera, generando una capa protectora para mantener la propia humedad de la carne. Por otro lado, cuando las verduras estén tan cocidas como desees, retíralas de la sartén y dora en el mismo fondo de cocción el pollo con la harina o con la maicena. No lo muevas hasta que esté dorado. Luego, dale la vuelta y déjalo que se dore por el otro lado. Señalamos que los muslos de pollo a la portuguesa también están deliciosos.<br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 3. Cuando hayas terminado de dorar el pollo en la olla, agrega las verduras salteadas, los condimentos, las arvejas, los tomates peritas pelados y la salsa de tomate. De esta forma, elaborarás una salsa portuguesa. Baja el fuego al mínimo y tapa la sartén. En 10-15 minutos el pollo estará cocido, ya podrás agregar un cuarto cucharadita de bicarbonato de sodio para que el tomate no esté ácido. Si estás preparando este plato con más tiempo, puedes cocinarlo por 40 minutos para que pierda la acidez y tome más sabor aún. ¡Ya casi tienes listo el pollo a la cacerola con papas!<br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 4. Sirve tu pollo a la portuguesa tradicional con puré o papas al horno. También puedes acompañarlo con arroz, fideos o batatas.  <br>
