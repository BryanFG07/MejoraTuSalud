        <div class="procedimiento">
            <center><h3>Procedimiento</h3></center>
            &nbsp;&nbsp;&nbsp;&nbsp;Paso 1. Ponemos las alubias a remojo en abundante agua entre cuatro y ocho horas o, mejor incluso, durante la noche anterior a preparar la ensalada. Escurrimos, enjuagamos y hervimos, partiendo de agua fría, con un chorrito de aceite de oliva hasta que estén tiernas. El tiempo dependerá de la calidad de la alubia y del tiempo de remojo previo. A modo de referencia, calculad unos 60-90 minutos.<br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 2. Una vez tiernas nuestras alubias, las escurrimos y refrescamos bajo un chorro de agua fría. Procurad tener cuidado con la presión para que no se rompan ni se desprendan sus pieles. Dejamos que las judías se enfríen y, mientras tanto, procedemos con el resto de los ingredientes.<br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 3. Escurrimos la ventresca y reservamos el aceite en el que viene conservada, ya que podemos usarlo para aliñar la ensalada. Lavamos y cortamos los tomates Cherry por la mitad. En un cuenco hondo y espacioso, los mezclamos con los berros (también podemos usar espinacas o brotes tiernos) y la ventresca. <br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 4. Exprimimos la media lima y batimos su zumo con dos cucharadas de aceite de oliva virgen extra (o con el de la ventresca, si optamos por aprovecharlo). Agregamos las judías que queramos añadir al resto de ingredientes y los aliñamos con esta mezcla. Sazonamos con escamas de sal maldon y removemos hasta repartir bien el aliño.  ¡A comer!<br>
