            <center><h3>Procedimiento</h3></center>
            &nbsp;&nbsp;&nbsp;&nbsp;Paso 1. Si te preguntas cómo hacer cuete mechado al horno, esta receta es perfecta para ti. Así pues, para empezar con la receta de cuete mechado navideño, primero pela y corta la cebolla y la zanahoria en pluma y rodajas, respectivamente. También, lava y corta en juliana el ají. A continuación, dora los ingredientes en una sartén amplia con 1 cucharadita de aceite. Cuando estén dorados, retíralos y disponlos en una asadera como colchón para tu cuete. <br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 2. En la misma sartén donde has salteado los vegetales, sella la carne con el fuego bien fuerte. Esto hará que los jugos queden concentrados en el centro gracias a la costra que se forma en la superficie de la carne.<br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 3. Una vez que hayas sellado todas las caras del cuete, apóyalo sobre los vegetales y levanta el fondo de cocción con el vino tinto, el ajo y los condimentos. <br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 4. Cuando el vino ya no tenga olor a alcohol, vuélcalo sobre la carne y los vegetales. Acto seguido, cocina tu cuete de res asado en el horno fuerte aproximadamente 30 minutos.<br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 5. Pasado ese tiempo, retira el cuete de res al horno para comprobar que todavía contiene líquido y gira la carne. Y, si necesita líquido, añade un poco de agua caliente y cocínalo 20-30 minutos más, dependiendo del tamaño de tu cuete. Apaga el horno, retíralo y déjalo reposar en la asadera 5 minutos. Sirve tu cuete al horno con una ensalada fresca y papas doradas al horno. ¡A comer!<br>
