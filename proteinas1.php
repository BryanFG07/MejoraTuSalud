        <div class="procedimiento">
            <center><h3>Procedimiento</h3></center>
            &nbsp;&nbsp;&nbsp;&nbsp;Paso 1. Pon quinoa, una taza y ¾ de agua y un par de pellizcos de sal en una cazuela mediana. Ponlo a hervir, reduce el fuego a medio-bajo y deja que hierva a fuego lento y cubierta hasta que el líquido se absorba y la quinoa esté blanda, aproximadamente 12 minutos. Déjala a un lado cubierta durante 5 minutos y después ahueca la quinoa con un tenedor.    <br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 2. Mezcla yogurt, aceite, aguacate, zumo de lima, pimiento chile chipotle y un par de pizcas de sal hasta que quede homogéneo.<br>
           <br> &nbsp;&nbsp;&nbsp;&nbsp;Paso 3. Divide el aliño de aguacate entre 4 tarros de boca ancha y después pon encima el pollo, las alubias negras, la quinoa, el maíz, los tomates, la cebolleta, la espinaca baby y las semillas de calabaza. ¡A comer!<br>
