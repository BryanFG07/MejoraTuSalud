            <center><h3>Procedimiento</h3></center>
            &nbsp;&nbsp;&nbsp;&nbsp;Paso 1. Calentamos un poco de aceite de oliva en una cazuela o sartén amplia. Troceamos y doramos la cebolla y cuando esté transparente añadimos los dientes de ajo laminados. Una vez estén dorados echamos la salsa de tomate, cocemos un minuto y a continuación ponemos el pimentón. Mezclamos y rehogamos un par de minutos teniendo cuidado de que el pimentón no se queme. <br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 2. Añadimos las espinacas limpias y escurridas y dejamos que poco a poco se vayan poniendo blandas al perder el agua que contienen. Cuando estén casi listas, y hayan perdido la mitad de su volumen inicial, echamos los garbanzos** para que se caliente el conjunto y se termine de hacer. Ponemos los piñones y las pasas, salteamos un par de minutos más y servimos bien caliente.<br>
