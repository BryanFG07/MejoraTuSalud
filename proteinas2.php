        <div class="procedimiento">
            <center><h3>Procedimiento</h3></center>
            &nbsp;&nbsp;&nbsp;&nbsp;Paso 1. Calienta aceite en una olla grande a temperatura media-alta. Añade los trozos de carne y cocínalos hasta que se doren por todas partes. Si la olla está demasiado llena, dora la carne por tandas. Saca la carne de la olla y resérvala. <br>
            <br>&nbsp;&nbsp;&nbsp;&nbsp;Paso 2. Reduce a temperatura media. Pon la cebolla, la zanahoria y la sal en la olla y caliéntalo hasta que la cebolla empiece a oscurecerse, unos 6 minutos. Añade la batata, el apio y el ajo a la olla; caliéntalo durante 3 minutos. Añade el concentrado de tomate, el tomillo, la pimienta negra y los copos de chile a la sartén y caliéntalo 1 minuto. Añade la cerveza, ponlo a hervir a fuego lento destapado durante 3 minutos, quita con la espátula cualquier trozo marrón del fondo de la olla. Pon el caldo, la carne, la espelta y la salsa Worcestershire en la olla. Ponlo a hervir, reduce a temperatura baja y cocina a fuego lento poco a poco cubierto durante 35 minutos, o hasta que la espelta esté blanda, añadiendo caldo adicional si fuese necesario. ¡A comer! <br>

